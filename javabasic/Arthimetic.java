public class Arthimetic {

    public static void main(String[] args) {
        
        // arithmetic operator

        int x = 10;
        int y = 2;
        int z;

        z = x - y; // 8
        z = x * y; // 20
        z = x + y; // 12
        z = x / y; // 5
        z = x % y; // 0


        // augmented assignment operators

        x += y;
        x -= y;
        x *= y;
        x /= y;
        x %= y;


        // Increment and Decrement Operator

        int x = 4;

        x++;  // 5
        x++;  // 6

        x--; // 5
        x--; // 4

        // Order of Operations   [P E M D A S] LEFT TO RIGHT

        double result = 3 + 4 * (4+5) / 2.0;

        






    }
}
